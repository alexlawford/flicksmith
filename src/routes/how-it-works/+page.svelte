        <h1>How it Works</h1>
        <h2>1. You Submit a Brief</h2>
        <p>The questionairre you’ll find if you follow the link on the homepage will take you through a step-by-step process to submit a simple brief. This helps directors know exactly what you’re looking for, speeding up the process of developing a pitch. <strong>The more details you can give at this stage, the better</strong></p>
        <h2>2. Flicksmith Reaches Out to Talented Directors</h2>
        <p>Next, Flicksmith reaches out to our network of talented directors, specifically choosing directors that are suited to your project in terms of content, budget, and other requirements. We help them develop a pitch quickly, but specifically tailored to your needs.</p>
        <p>Sometimes, before we get to this stage, one of our team will reach out to you to clarify some details.</p>
        <h2>3. Directors Respond with “Treatments”</h2>
        <p>Between two and three directors will respond with what’s called a <em>treatment</em>.</p>
        <p>A treatment breaks down a creative direction made in response to your brief.</p>
        <p>It can include a story outline and references for look, wardrobe, actors, locations, and cinematography. It will often include details of what shots and other creative choices the director would like to use to bring your brief to life.</p>
        <p>In short, it’s a comprehensive creative vision for your project.</p>
        <p>Alongside the treatment, Flicksmith will also attatch:</p>
        <ul>
            <li>An introduction to the director’s previous work</li>
            <li>A budget</li>
            <li>A suggested timeline</li>
        </ul>
        <h2>4. You Choose the Treatment You Like</h2>
        <p>Presented with these options, you’ll pick the director you like. Details don’t have to be set in stone yet, don’t worry &mdash; there’ll be plenty of time to refine the approach with your chosen director once the project gets started.</p>
        <h2>5. Begin Pre-Production</h2>
        <p>Finally, your chosen director begins working on your film. Then it’s time to starting making your project! The director and a production team will start working alongside you to make your film a reality.</p>
